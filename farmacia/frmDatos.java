    public void CargarDatos(){
        Producto p;
        for(int i=0;i<Ventanaprincipal.contenedor.size();i++){
            p=(Producto)Ventanaprincipal.contenedor.get(i);
            modelo.insertRow(contador, new Object[]{});
            modelo.setValueAt(p.getNombredelproducto(), contador, 0);
            modelo.setValueAt(p.getTipodelproducto(), contador, 1);
            modelo.setValueAt(p.getCantidaddelproducto(), contador, 2);
            modelo.setValueAt(p.getNombredelproveedor(), contador, 3);
            modelo.setValueAt(p.getSucursal(), contador, 4);
        }
    }
